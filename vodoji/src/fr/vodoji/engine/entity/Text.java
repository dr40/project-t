package fr.vodoji.engine.entity;

import fr.vodoji.engine.drawable.DrawOptions;
import fr.vodoji.engine.graphics.Font;
import fr.vodoji.engine.graphics.Screen;

/**
 * 
 * @author Dorian RODDE, Vivian RODDE
 */
public class Text extends Entity {
	
	///////////////////////////////////////////////////////////////////////////////////////////
	// Members
	///////////////////////////////////////////////////////////////////////////////////////////

	protected Font _font;
	protected String _text;
	
	
	///////////////////////////////////////////////////////////////////////////////////////////
	// Constructors
	///////////////////////////////////////////////////////////////////////////////////////////

	public Text() {
		;
	}
	public Text(String text, Font fnt) {
		_text = text;
		_font = fnt;
	}
	
	
	///////////////////////////////////////////////////////////////////////////////////////////
	// Getters
	///////////////////////////////////////////////////////////////////////////////////////////
	
	public String getText() {
		return _text;
	}
	public Font getFont() {
		return _font;
	}
	
	
	///////////////////////////////////////////////////////////////////////////////////////////
	// Setters
	///////////////////////////////////////////////////////////////////////////////////////////

	public void setText(String text) {
		_text = text;
	}
	public void setFont(Font fnt) {
		_font = fnt;
	}
	
	
	///////////////////////////////////////////////////////////////////////////////////////////
	// Draw methods
	///////////////////////////////////////////////////////////////////////////////////////////

	@Override
	public void draw(Screen toScreen) {
		if ((_font != null) && (_text != null)) {
			toScreen.drawText(_text, _x, _y, _font);
		}
	}

	@Override
	public void draw(Screen toScreen, float x, float y) {
		if ((_font != null) && (_text != null)) {
			toScreen.drawText(_text, x, y, _font);
		}
	}

	@Override
	public void draw(Screen toScreen, DrawOptions opt) {
		if ((_font != null) && (_text != null)) {
			toScreen.drawText(_text, opt.getX(), opt.getY(), _font);
		}
	}

}
