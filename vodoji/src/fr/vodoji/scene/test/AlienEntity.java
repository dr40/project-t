package fr.vodoji.scene.test;

import java.util.Random;

import fr.vodoji.engine.entity.Sprite;


public class AlienEntity extends Sprite {

	///////////////////////////////////////////////////////////////////////////////////////////
	// Members
	///////////////////////////////////////////////////////////////////////////////////////////
	
	protected float vx;
	protected float vy;

	///////////////////////////////////////////////////////////////////////////////////////////
	// Constructors
	///////////////////////////////////////////////////////////////////////////////////////////

	public AlienEntity() {
		vx = new Random().nextFloat()*8-4;
		vy = new Random().nextFloat()*8-4;
	}
	

	///////////////////////////////////////////////////////////////////////////////////////////
	// Events
	///////////////////////////////////////////////////////////////////////////////////////////

	public void update() {
		_x += vx;
		_y += vy;
		if (_x < 0) {
			_x = 0;
			vx = -vx;
		}
		if (_x > 320) {
			_x = 320;
			vx = -vx;
		}
		if (_y < 0) {
			_y = 0;
			vy = -vy;
		}
		if (_y > 480) {
			_y = 480;
			vy = -vy;
		}
	}
	
}
