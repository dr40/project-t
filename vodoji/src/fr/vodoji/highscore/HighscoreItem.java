package fr.vodoji.highscore;

import java.util.Date;

/**
 * 
 * @author Dorian RODDE, Vivian RODDE
 */
public class HighscoreItem {

	///////////////////////////////////////////////////////////////////////////////////////////
	// Protected members
	///////////////////////////////////////////////////////////////////////////////////////////

	protected String _userName;
	protected int _score;
	protected int _level;
	protected Date _scoreDate;

	///////////////////////////////////////////////////////////////////////////////////////////
	// Public constructors
	///////////////////////////////////////////////////////////////////////////////////////////

	public HighscoreItem(String username, int level, int score) {
		_userName = username;
		_level = level;
		_score = score;
		_scoreDate = new Date();
	}
	public HighscoreItem(String username, int level, int score, long scoreTimestamp) {
		_userName = username;
		_level = level;
		_score = score;
		_scoreDate = new Date(scoreTimestamp);
	}

	///////////////////////////////////////////////////////////////////////////////////////////
	// Getters
	///////////////////////////////////////////////////////////////////////////////////////////

	public String getUserName() {
		return _userName;
	}
	public int getLevel() {
		return _level;
	}
	public int getScore() {
		return _score;
	}
	public Date getScoreDate() {
		return _scoreDate;
	}

	///////////////////////////////////////////////////////////////////////////////////////////
	// ToString
	///////////////////////////////////////////////////////////////////////////////////////////

	@Override
	public String toString() {
		return  _userName + "> (" + _scoreDate + ")" + _score + " on level <" + _level + ">";
	}

}
